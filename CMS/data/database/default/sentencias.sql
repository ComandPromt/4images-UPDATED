CREATE TABLE tbl_tracking (
id_tracking int(11) PRIMARY KEY AUTO_INCREMENT,
tx_pagina varchar(70) NOT NULL DEFAULT '',
tx_paginaOrigen varchar(200) NOT NULL DEFAULT '',
